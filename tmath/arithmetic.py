def add (arg1, arg2):
    return arg1 + arg2
    
def subtract(arg1, arg2):
    return arg1 - arg2

def multiply(arg1, arg2):
    return arg1 * arg2
    
def divide(arg1, arg2):
    return arg1 / arg2

def modulus(arg1, arg2):
    return arg1 % arg2
    
